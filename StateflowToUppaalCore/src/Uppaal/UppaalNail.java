package Uppaal;

public class UppaalNail {
	
	private int x;
	private int y;
	
	public UppaalNail(int x,int y) {
		this.setX(x);
		this.setY(y);
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}
	
}